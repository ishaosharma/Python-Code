for _ in range(3):
    print("Hello")

# The expression for _ in range(n): in Python means:

# Run a loop exactly n times.

# Use _ as the loop variable name to indicate that the variable’s value is not important and won't be used inside the loop.

# The underscore _ is a convention in Python for “I don’t care about this value.”

# This keeps the code clean by showing clearly that the loop is just to repeat an action n times without needing the iteration index.

# Simple explanation:
# You want to repeat something (like calling yield or printing) multiple times but don’t need the actual counter number. 
# Using _ instead of, say, i or count signals that the value itself is irrelevant.